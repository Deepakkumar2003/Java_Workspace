package com.jsp.String;

public class CharSequence26 {

	public static void main(String[] args) {
		String s="javadev";
		String s1="dev";
		String s3="java"+"dev";
		String s4="java"+s1;
		System.out.println(s);
		System.out.println(s3);
		System.out.println(s4);
		System.out.println(s==s3);
		System.out.println(s==s4);
	}

}
